振り子は、重力と張力という二つの力の作用によって運動します。小さな振幅で揺れる振り子の運動は、「単振動」と呼ばれる特別な種類の往復運動とみなすことができます。単振動では、物体が平衡点から離れるほど、平衡点に戻ろうとする力が大きくなり、その力は平衡点からの変位に比例します。

